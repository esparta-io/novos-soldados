    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (!jList2.isSelectionEmpty()) {
            this.listPendencia.addElement(jList2.getSelectedValue());
            jList1.setModel(listPendencia);
            this.listFinalizada.remove(jList2.getSelectedIndex());
        }        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!jTextField1.getText().isEmpty()) {
            this.listPendencia.addElement(jTextField1.getText());
            jList1.setModel(listPendencia);
        }
        jTextField1.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (!jList1.isSelectionEmpty()) {
            this.listFinalizada.addElement(jList1.getSelectedValue());
            jList2.setModel(listFinalizada);
            this.listPendencia.remove(jList1.getSelectedIndex());
        }

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        if (!jList1.isSelectionEmpty()) {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir?", "Excluir tarefa", dialogButton);
            if (dialogResult == 0) {
                this.listPendencia.remove(jList1.getSelectedIndex());
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed


